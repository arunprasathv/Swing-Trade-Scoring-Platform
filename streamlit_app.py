# ---- Market Context (Always Visible) ----
try:
    # Get SPY data
    spy = yf.Ticker("SPY")
    spy_hist = spy.history(period="2d")
    prev_close = float(spy_hist['Close'].iloc[-2])
    spy_price = float(spy_hist['Close'].iloc[-1])
    price_change = spy_price - prev_close
    
    # Get analysis
    spy_analysis = analyze_ticker("SPY")
    spy_score = spy_analysis["SPY/SPX Momentum Score"]
    
    # Calculate and format change
    delta = f"{'+' if price_change > 0 else ''}{price_change:.2f}"
    delta_color = "normal" if price_change > 0 else "inverse"
    
    # Display market context
    col1, col2 = st.columns([1, 2])
    with col1:
        st.metric("üåé Market Score", f"{spy_score}/10", "SPY/SPX")
    with col2:
        st.metric("üìä SPY Price", f"${spy_price:.2f}", delta, delta_color=delta_color)
        
except Exception as e:
    st.error(f"Error getting market context: {str(e)}")

# ---- Input Section ----
input_container = st.container()
with input_container:
    col1, col2, col3, col4 = st.columns([3,2,2,1])
    with col1:
        tickers_input = st.text_input("üìù Enter Tickers (comma-separated)", value="MSFT, NVDA, AAPL")
    with col2:
        min_tech_score = st.slider("üìä Min Technical Score", 0.0, 10.0, 5.0, 0.5)
    with col3:
        min_rr = st.slider("‚öñÔ∏è Min Risk/Reward", 0.0, 3.0, 1.0, 0.1)
    with col4:
        st.write("")  # Spacing
        st.write("")  # Spacing
        scan_button = st.button("üîç Scan")
        
if scan_button:
    tickers = [t.strip().upper() for t in tickers_input.split(",") if t.strip()]
    progress = st.progress(0)
    
    results = []
    for idx, ticker in enumerate(tickers, 1):
        try:
            analysis = analyze_ticker(ticker)
            
            # Only include if meets minimum criteria
            tech_score = float(analysis["Ticker Technical Score"])
            rr = float(analysis["R:R (to TP1)"])
            
            if tech_score >= min_tech_score and rr >= min_rr:
                results.append(analysis)
                
        except Exception as e:
            st.error(f"Error analyzing {ticker}: {str(e)}")
            
        progress.progress(idx/len(tickers))
    
    if results:
        # Convert to DataFrame and sort by multiple criteria
        df = pd.DataFrame(results)
        df = df.sort_values(
            by=["Ticker Technical Score", "R:R (to TP1)", "Success Probability (%)"],
            ascending=[False, False, False]
        )
        
        # Show sorting criteria
        st.info("üìä Results sorted by: Technical Score ‚Üí Risk/Reward ‚Üí Success Probability")
        
        # Display results in sections
        for _, row in df.iterrows():
            # Pattern-based color and icon
            pattern = row['Strategy Type']
            if "bullish" in pattern.lower():
                pattern_color = "üü¢"
            elif "bearish" in pattern.lower():
                pattern_color = "üî¥"
            elif "neutral" in pattern.lower() or "consolidation" in pattern.lower():
                pattern_color = "üü°"
            else:
                pattern_color = "‚ö™"
                
            # Get price change
            ticker = row['Ticker']
            current_price = row['Price']
            ticker_hist = yf.Ticker(ticker).history(period="2d")
            price_text = f"${current_price:.2f}"
            
            if len(ticker_hist) >= 2:
                prev_close = float(ticker_hist['Close'].iloc[-2])
                price_change = current_price - prev_close
                pct_change = (price_change / prev_close) * 100
                pattern_color = "üü¢" if price_change > 0 else "üî¥"
                
                sign = "+" if price_change > 0 else ""
                arrow = "‚Üë" if price_change > 0 else "‚Üì"
                price_text = f"${current_price:.2f} {arrow} ({sign}${abs(price_change):.2f}, {sign}{pct_change:.1f}%)"
            
            # Create header text
            header = f"{pattern_color} {row['Ticker']} {price_text} - {row['Strategy Type']} ({row['Ticker Technical Score']}/10)"
            
            # Create expander
            expander = st.expander(header)
            with expander:
                cols = st.columns(3)
                
                # Column 1: Pattern & Scores
                with cols[0]:
                    st.markdown("### üìà Pattern Analysis")
                    
                    # Pattern classification
                    pattern_type = row['Strategy Type'].split()[0].lower()
                    if "consolidation" in pattern_type:
                        st.info(f"üì¶ {row['Strategy Type']}")
                    elif "momentum" in pattern_type:
                        st.success(f"üöÄ {row['Strategy Type']}")
                    elif "reversal" in pattern_type:
                        st.warning(f"‚Ü©Ô∏è {row['Strategy Type']}")
                    elif "testing" in pattern_type:
                        st.info(f"üéØ {row['Strategy Type']}")
                    elif "mean" in pattern_type:
                        st.warning(f"‚ÜïÔ∏è {row['Strategy Type']}")
                    else:
                        st.write(f"‚è≥ {row['Strategy Type']}")
                        
                    st.metric("Technical Score", f"{row['Ticker Technical Score']}/10")
                    st.metric("Sector Score", f"{row['Sector Strength Score']}/10")
                
                # Column 2: Trade Setup
                with cols[1]:
                    st.markdown("### üéØ Trade Setup")
                    
                    # Entry Zone with tooltip
                    col1, col2 = st.columns([6, 1])
                    with col1:
                        st.write(f"Entry Zone: {row['Entry Zone']}")
                    with col2:
                        st.help("""
                        Entry Zone = EMA21 ¬± (0.3 √ó ATR)
                        ‚Ä¢ Calculated around 21-day moving average
                        ‚Ä¢ ¬±30% of ATR provides flexibility
                        ‚Ä¢ Best entry: lower end of range
                        ‚Ä¢ Waits for pullback to key support
                        """)
                    
                    # Stop Loss with tooltip
                    col1, col2 = st.columns([6, 1])
                    with col1:
                        st.write(f"Stop Loss: {row['Stop Loss']}")
                    with col2:
                        st.help("""
                        Stop Loss = Recent swing low or nearest EMA
                        ‚Ä¢ Uses 5-day swing low
                        ‚Ä¢ Or EMA21/EMA50 if price is above them
                        ‚Ä¢ No ATR buffer added (tighter risk)
                        ‚Ä¢ Protects against breakdown
                        """)
                    
                    # Targets with tooltip
                    col1, col2 = st.columns([6, 1])
                    with col1:
                        st.write(f"Targets: {row['TP1/TP2']}")
                    with col2:
                        st.help("""
                        TP1 = Current Price + Recent Range
                        TP2 = TP1 + ATR
                        
                        ‚Ä¢ TP1: Recovers recent 5-day decline
                        ‚Ä¢ TP2: Extended target with momentum
                        ‚Ä¢ Based on actual price behavior
                        ‚Ä¢ Volatility-adjusted using ATR
                        """)
                    
                    # Risk/Reward with tooltip
                    col1, col2 = st.columns([6, 1])
                    with col1:
                        st.metric("Risk/Reward", str(row['R:R (to TP1)']))
                    with col2:
                        st.help("""
                        R:R = (TP1 - Current Price) / (Current Price - Stop)
                        
                        ‚Ä¢ Measures potential reward vs risk
                        ‚Ä¢ Minimum 1:1 for viable trades
                        ‚Ä¢ 2:1+ considered good setups
                        ‚Ä¢ Capped at 5:1 to avoid unrealistic ratios
                        """)
                
                # Column 3: Technical Details
                with cols[2]:
                    st.markdown("### üìä Technical Details")
                    st.write(f"RSI(14): {row['RSI(14)']}")
                    st.write(f"ATR(14): ${row['ATR(14)']}")
                    
                    # Parse EMAs and display individually with price comparison
                    emas = row['EMA9/21/50'].split(" / ")
                    current_price = row['Price']
                    st.write("EMAs:")
                    
                    # Compare price with each EMA
                    ema9 = float(emas[0])
                    ema21 = float(emas[1])
                    ema50 = float(emas[2])
                    
                    st.write(f"‚Ä¢ 9: ${ema9:.2f} {'‚úÖ' if current_price > ema9 else '‚ùå'}")
                    st.write(f"‚Ä¢ 21: ${ema21:.2f} {'‚úÖ' if current_price > ema21 else '‚ùå'}")
                    st.write(f"‚Ä¢ 50: ${ema50:.2f} {'‚úÖ' if current_price > ema50 else '‚ùå'}")
                    
                    # Calculate volume metrics
                    hist = yf.Ticker(row['Ticker']).history(period="30d")
                    avg_vol_20d = hist['Volume'].iloc[-20:].mean()
                    current_vol = hist['Volume'].iloc[-1]
                    vol_ratio = (current_vol / avg_vol_20d) * 100
                    
                    # Format volume numbers
                    def format_volume(vol):
                        if vol >= 1_000_000:
                            return f"{vol/1_000_000:.1f}M"
                        elif vol >= 1_000:
                            return f"{vol/1_000:.1f}K"
                        return str(int(vol))
                    
                    st.write("")  # Add spacing
                    st.write("Volume Analysis:")
                    
                    # Volume display with scoring impact
                    if vol_ratio > 100:
                        vol_impact = "Bullish üìà" if price_change > 0 else "Bearish üìâ"
                        st.write(f"‚Ä¢ Today: {format_volume(current_vol)} (‚Üë {vol_ratio:.0f}% of avg)")
                        st.caption(f"Volume expansion with {vol_impact} price action")
                    else:
                        st.write(f"‚Ä¢ Today: {format_volume(current_vol)} (‚Üì {vol_ratio:.0f}% of avg)")
                        st.caption("Volume below average - reduced conviction")
                        
                    st.write(f"‚Ä¢ 20d Avg: {format_volume(avg_vol_20d)}")
                    
                    # Volume scoring explanation
                    st.write("")  # Add spacing
                    st.write("‚ÑπÔ∏è Volume Impact on Score:")
                    st.caption("""
                    ‚Ä¢ Above average volume (>100%) adds conviction
                    ‚Ä¢ Volume expansion with price trend adds score
                    ‚Ä¢ Low volume reduces pattern confidence
                    ‚Ä¢ Volume trend aligned with price trend increases score
                    """)
        
        # Add download button at bottom
        st.write("")  # Add spacing
        csv = df.to_csv(index=False)
        st.download_button(
            "üì• Download Full Analysis",
            csv,
            "stratify_analysis.csv",
            "text/csv",
            key='download-csv',
            help="Download detailed analysis in CSV format"
        )
        
    else:
        st.warning("No tickers met the minimum criteria.")

